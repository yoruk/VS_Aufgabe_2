#ifndef CAF_ACTOR_CAST_HPP
#define CAF_ACTOR_CAST_HPP

namespace caf {

/**
 * Converts actor handle `what` to a different actor handle of type `T`.
 */
template <class T, typename U>
T actor_cast(const U& what) {
  return what.get();
}

} // namespace caf

#endif // CAF_ACTOR_CAST_HPP

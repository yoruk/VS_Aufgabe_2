#ifndef CPPA_ANY_TUPLE_HPP
#define CPPA_ANY_TUPLE_HPP

#include "caf/message.hpp"

namespace caf {

using any_tuple = message;

template <class... Ts>
any_tuple make_any_tuple(Ts&&... ts) {
  return make_message(std::forward<Ts>(ts)...);
}

} // namespace caf

#endif // CPPA_ANY_TUPLE_HPP
